{
    unsigned char c = 0, i = 0, j = 0;
    while (ch[j] != '\0')
    {
        c = ch[j] - 32;
        if(x > 126)
        {
            x = 0;
            y++;
        }
        LCD_Set_Pos(x, y);
        if(c > 0 && c < 92)
        {
            for(i = 0; i < 6; i++)
                LCD_WrDat(F6x8[c][i]);
        }
        x += 6;
        j++;
    }
}

