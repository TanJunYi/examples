void LCD_WrDat(unsigned char dat)
{
    unsigned char i=8, temp=0;
    GPIOPinWrite(LCD_DC, 1);
    for(i=0; i<8; i++)
    {
        GPIOPinWrite(LCD_SCL, 0);

        temp = dat&0x80;
        if (temp == 0)
        {
            GPIOPinWrite(LCD_SDA, 0);
        }
        else
        {
            GPIOPinWrite(LCD_SDA, 1);
        }
        GPIOPinWrite(LCD_SCL, 1);
        dat<<=1;
    }
}

void LCD_WrCmd(unsigned char cmd)
{
    unsigned char i=8, temp=0;
    GPIOPinWrite(LCD_DC, 0);
    for(i=0; i<8; i++)
    {
        GPIOPinWrite(LCD_SCL, 0);

        temp = cmd&0x80;
        if (temp == 0)
        {
            GPIOPinWrite(LCD_SDA, 0);
        }
        else
        {
            GPIOPinWrite(LCD_SDA, 1);
        }
        GPIOPinWrite(LCD_SCL, 1);
        cmd <<= 1;
    }
}

void LCD_Set_Pos(unsigned char x, unsigned char y)
{
    LCD_WrCmd(0xb0+y);
    LCD_WrCmd(((x&0xf0)>>4)|0x10);
    LCD_WrCmd((x&0x0f)|0x01);
}

void LCD_Fill(unsigned char bmp_dat)
{
    unsigned char y,x;
    for(y=0; y<8; y++)
    {
        LCD_WrCmd(0xb0+y);
        LCD_WrCmd(0x01);
        LCD_WrCmd(0x10);
        for(x=0; x<X_WIDTH; x++)
            LCD_WrDat(bmp_dat);
    }
}

void LCD_FillLine(unsigned char y,unsigned char ch)
{
    unsigned char x;
    LCD_WrCmd(0xb0 + y);
    LCD_WrCmd(0x01);
    LCD_WrCmd(0x10);
    for(x = 0; x < X_WIDTH; x++)
        LCD_WrDat(ch);
}

void LCD_CLS(void)
{
    unsigned char y, x;
    for(y = 0; y < 8; y++)
    {
        LCD_WrCmd(0xb0 + y);
        LCD_WrCmd(0x01);
        LCD_WrCmd(0x10);
        for(x=0; x < X_WIDTH; x++)
            LCD_WrDat(0);
    }
}

